const LOGO_GRADIENT = [0, 0, 1, 1, 2, 2] as const
const CADUC_GRADIENT = [2, 2, 1, 1, 0, 0, 1, 1, 2, 2, 3, 3, 3, 3, 3] as const

const colorize = (art: string[], gradient: readonly number[], c: ThemeColors): Line[] => {
  const p = [c.primary, c.accent, c.border, c.muted]

  return art.map((text, i) => [p[gradient[i]!] ?? c.muted, text])
}

const CUSTOM_HERO_MIN_WIDTH = 28

const centerLine = (text: string, width: number) => {
  const clipped = text.length > width ? text.slice(0, width) : text
  const slack = Math.max(0, width - clipped.length)
  const left = Math.floor(slack / 2)

  return `${' '.repeat(left)}${clipped}${' '.repeat(slack - left)}`
}

const customHero = (markup: string, c: ThemeColors): Line[] => {
  if (parseRichMarkup(markup).some(([color]) => color)) {
    return parseRichMarkup(markup)
  }

  // Plain custom hero art is hand-spaced terminal art. Preserve trailing cells so
  // callers can intentionally bias individual rows inside the fixed hero block.
  const art = markup.replace(/\r/g, '').split('\n')
  const customWidth = Math.max(CUSTOM_HERO_MIN_WIDTH, ...art.map(line => line.length))
  const normalized = [
    ...art.map(line => centerLine(line, customWidth)),
    ...Array(Math.max(0, CADUC_GRADIENT.length - art.length)).fill(' '.repeat(customWidth))
  ]

  return colorize(normalized.slice(0, CADUC_GRADIENT.length), CADUC_GRADIENT, c)
}

export const LOGO_WIDTH = Math.max(...LOGO_ART.map(line => line.length))
export const CADUCEUS_WIDTH = Math.max(...CADUCEUS_ART.map(line => line.length))

export const logo = (c: ThemeColors, customLogo?: string): Line[] =>
  customLogo ? parseRichMarkup(customLogo) : colorize(LOGO_ART, LOGO_GRADIENT, c)

export const caduceus = (c: ThemeColors, customHeroMarkup?: string): Line[] =>
  customHeroMarkup ? customHero(customHeroMarkup, c) : colorize(CADUCEUS_ART, CADUC_GRADIENT, c)

export const artWidth = (lines: Line[]) => lines.reduce((m, [, t]) => Math.max(m, t.length), 0)

type Line = [string, string]
